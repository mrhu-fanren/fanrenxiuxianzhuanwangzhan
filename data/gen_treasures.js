function pick(arr){ return arr[Math.floor(Math.random()*arr.length)]; }
function rand(min,max){ return Math.floor(Math.random()*(max-min+1))+min; }

// 生成组合名（避免重复）
function genName(){
  var pre = pick(prefixes);
  var body = pick(bodies);
  // 约 30% 概率加后缀
  var useSuffix = Math.random() < 0.3;
  var name = useSuffix ? (pre + body + pick(suffixes)) : (pre + body);
  return name;
}

// 用确定性 hash 让 desc 与名字相关，避免完全随机感
function hashStr(s){
  var h = 0;
  for(var i=0;i<s.length;i++){ h = (h*31 + s.charCodeAt(i)) >>> 0; }
  return h;
}
function makeDesc(name, type, attr){
  var h = hashStr(name + type + attr);
  var templ = effectTempl[h % effectTempl.length];
  var eff = templ.replace(/\{attr\}/g, attr);
  return name + "乃修仙界中颇有名气的" + type + "，" + eff + "。此物来历不俗，历经多番争夺方才现世，为修士所珍藏。";
}

